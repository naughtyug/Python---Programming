number_x = int(input("Enter a value for number_x: "))
number_y = int(input("Enter a value for number_y: "))

if number_x > number_y:
    print("number_x is greater than number_y")
elif number_x < number_y:
    print("number_x is less than number_y")
else:
    print("number_x is equal to number_y")